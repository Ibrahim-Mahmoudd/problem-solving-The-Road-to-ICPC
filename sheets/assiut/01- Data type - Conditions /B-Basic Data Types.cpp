#include<iostream>
using namespace std;
int main () {
    int i ;
    long long l ;
    char  c ;
    float f ;
    double d ;
    cin >>i>>l>>c>>f>>d ;
    cout <<i<<"\n"<<l<<"\n"<<c<<"\n"<<f<<"\n"<<d ;
    
    
    return 0;
}
